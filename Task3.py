"""
Ошибки в коде Вани:
1. max_number должен быть начально установлен на минимальное значение, например, на -1 
(или на любое другое значение, которое будет гарантированно меньше любого элемента 
последовательности).
2. if max_number > n: должно быть if max_number < n: для корректного поиска максимального 
элемента.

Ошибки в коде Пети:
1. Условие while n < 0: неверно. Цикл должен продолжаться, пока n не равно 0.
2. Внутри if условие n = max_number неверно, нужно присвоить max_number = n.
"""

# Корректное решение:

def find_max_until_zero():
    max_number = -1

    while True:
        n = int(input("Введите число (0 для завершения): "))
        if n == 0:
            break
        if n > max_number:
            max_number = n

    print("Наибольшее число в последовательности:", max_number)

# Вызов функции для выполнения задачи
find_max_until_zero()